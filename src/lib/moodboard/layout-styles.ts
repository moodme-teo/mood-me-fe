// FNV-1a 32bit — 암호학적 성질은 필요 없고 결정론적이기만 하면 된다(문서 §선택 방식).
function fnv1aHash(input: string): number {
  let hash = 0x811c9dc5;
  for (let i = 0; i < input.length; i++) {
    hash ^= input.charCodeAt(i);
    hash = Math.imul(hash, 0x01000193);
  }
  return hash >>> 0;
}

// final · shadows · transitions.picked를 각각 정렬해 이어붙인다 — 카드를 담은 순서는
// 구조에 영향을 주면 안 된다(같은 선택을 다른 순서로 담아도 같은 레이아웃이어야 함).
function canonicalJourney(journey: Journey): string {
  const final = [...journey.final].sort().join(",");
  const shadows = [...journey.shadows].sort().join(",");
  const picked = journey.transitions
    .map((t) => t.picked)
    .sort()
    .join(",");
  return `${final}|${shadows}|${picked}`;
}

export function pickLayoutStyle(journey: Journey): LayoutStyle {
  const hash = fnv1aHash(canonicalJourney(journey));
  const index = hash % MOODBOARD_LAYOUT_STYLES.length;
  return MOODBOARD_LAYOUT_STYLES[index];
}
